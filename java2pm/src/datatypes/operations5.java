package datatypes;

public class operations5 {
	
public static void main(String[] args) {
		
		int n1 = 100;
		int n2 = 80;
		 
		int sum = n1 + n2;
		int sub = n1 - n2;
		int mul = n1 * n2;
		int div = n1/n2;
		int remainder = n1%n2;
		                                              
		
	    // concatenation or join ; +used for join operations
	    System.out.println("sum of "+ n1 + "and" +n2 + "is" + sum);
		System.out.println("sub of "+ n1 + "and" +n2 + "is" + sub);
		System.out.println("mul of "+ n1 + "and" +n2 + "is" + mul);
		System.out.println("div of "+ n1 + "and" +n2 + "is" + div);
		System.out.println("remainder of "+ n1 + "and" +n2 + "is" + remainder);
}
} 


