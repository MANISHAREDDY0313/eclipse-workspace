package datatypes;

public class operations4 {
	
public static void main(String[] args) {
		
		int n1 = 100;
		int n2 = 80;
		
		int sum = n1 + n2;
		int sub = n1 - n2;
		int mul = n1 * n2;
		int div = n1/n2;
		int remainder = n1%n2;
		                                              
		
	    // concatenation or join ; +used for join operations
	    System.out.println("sum value is" + sum);
		System.out.println("sub result is" + sub);
		System.out.println("mul result is" + mul);
		System.out.println("div result is" + div);
		System.out.println("remainder is " + remainder);
}
}

